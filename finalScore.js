function finalScore(omr) {
  let totalMarks = 0;
  for (let count in omr) {
    totalMarks += omr[count];
  }
  if (totalMarks !== 100) {
    return "Invalid";
  }
  let correctAns = 1 * omr["right"];
  let wrongAns = 0.5 * omr["wrong"];
  let finalMarks = Math.round(correctAns - wrongAns);
  return finalMarks;
}
let finalResult = finalScore({ right: 67, wrong: 23, skip: 10 });
